package com.topspot;

public class ParseBean 
{
	private String URL = "";
	private String Category = "";
	private String Rent_Sales = "";
	
	public String getURL() {
		return URL;
	}
	public void setURL(String uRL) {
		URL = uRL;
	}
	public String getCategory() {
		return Category;
	}
	public void setCategory(String category) {
		Category = category;
	}
	public String getRent_Sales() {
		return Rent_Sales;
	}
	public void setRent_Sales(String rent_Sales) {
		Rent_Sales = rent_Sales;
	}
}
